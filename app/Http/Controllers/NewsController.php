    public function messageListApi()
    {
        $date = Carbon::today();
//        $news = News::all();
        $news = News::where('start_date', '<=', $date->copy()
            ->startOfDay())
            ->where('end_date', '>=', $date->copy()
                ->endOfDay())
            ->get();
        NewsCollection::wrap('list');

        return new NewsCollection($news);
    }
